public class Paragraph {
    public static void main(String[] args) {
        System.out.println("A well-formed Java program has a main method with { and } braces.");
        System.out.println("A System.out.println statement has ( and ) and usually a String that starts and");
        System.out.println("ends with a \" character.");
        System.out.println("(But we type \\\" instead!)");
        System.out.println("What is the difference between");
        System.out.println("a ' and a \"?  Or between a \" and a \\\"?");
        System.out.println("One is what we see when we're typing our program. The other is what appears");
        System.out.println("on the \"console.\"");
    }
}

